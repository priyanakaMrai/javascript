let a = 10;
let b = 5;

// Add
let add = a + b;

// Subtract
let subtract = a - b;

// Multiply
let multiply = a * b;

// Divide
let divide = a / b;

// Increment
a++;

// Decrement
b--;

// Remainder
let remainder = a % b;

console.log("Addition:", add);
console.log("Subtraction:", subtract);
console.log("Multiplication:", multiply);
console.log("Division:", divide);
console.log("Incremented a:", a);
console.log("Decremented b:", b);
console.log("Remainder:", remainder);
